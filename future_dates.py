user_input = int(input("enter today's day(0 to 6): "))
while user_input <= 6:
    if user_input == 0:
        print("Today is Sunday")
    elif user_input == 1:
        print("Monday")
    elif user_input == 2:
        print("Tuesday")
    elif user_input == 3:
        print("Wednesday")
    elif user_input == 4:
        print("Thursday")
    elif user_input == 5:
        print("Friday")
    elif user_input == 6:
        print("Saturday")
    break
user_input = int(input("enter a number(0 to 6): "))


